function getColor(info) {
    var colorList = {};
    info.forEach(function (member) {
        var name = member.memberName;

        colorList[name] = {
            "backgroundColor": member.backgroundColor,
            "borderColor": member.borderColor
        }
    });
    return colorList;
}

function getColorList(nameList) {

    var backgroundColor = [];
    var borderColor = [];
    var colorList = chartColorInfo;

    nameList.forEach(function (name) {
        backgroundColor.push(colorList[name]["backgroundColor"]);
        borderColor.push(colorList[name]["borderColor"]);
    });
    
    return {
        "backgroundColor": backgroundColor,
        "borderColor": borderColor
    }
}